function love.conf(t)
    t.window.width = 1000        -- Set the window width to 1000 pixels
    t.window.height = 800        -- Set the window height to 800 pixels
    t.window.title = "Test Game" -- Set the window title
    t.window.resizable = true    -- Allow the window to be resized
    t.window.vsync = 1           -- Enable vertical sync
end
